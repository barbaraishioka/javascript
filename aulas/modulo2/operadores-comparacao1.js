/* 

OPERADORES DE COMPARAÇÃO - 1 parte

== -> Igual, compara o valor
=== -> Igual, compara o valor e tipo

!= -> Diferente, compara o valor
!== -> Diferente, compara o valor e tipo

*/

const a = 10;
const b = "10";

console.log(a == b); // true
console.log(a === b); // false
console.log(a != b); // false
console.log(a !== b); // true
