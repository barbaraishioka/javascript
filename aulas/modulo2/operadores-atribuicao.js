/*

OPERADORES DE ATRIBUIÇÃO

= -> Atribuição
+= -> Adição
-= -> Subtração
/= -> Divisão
*= -> Multiplicação
%= -> Resto

*/

let [num1, num2, num3, num4, num5] = [20, 20, 20, 20, 20];

console.log((num1 += 20)); // num1 = num1 + 20 -> resultado 40
console.log((num2 -= 10)); // num2 = num2 - 10 -> resultado 10
console.log((num3 /= 2)); // num3 = num3 / 2 -> resultado 10
console.log((num4 *= 2)); // num4 = num4 * 2 -> resultado 40
console.log((num5 %= 2)); // num5 = num5 % 2 -> resultado 0
