/* 

SORT -> Organiza o array

*/

const array = ["limão", "abóbora", "caqui"];
array.sort();
console.log(array); // [ 'abóbora', 'caqui', 'limão' ]
// No caso acima, organizou por ordem alfabética

// _________________________________________

const numbers = [60, 30, 20, 10, 50, 40];
numbers.sort();
console.log(numbers); // [ 10, 20, 30, 40, 50, 60 ]
// No caso acima, organizou em ordem crescente
